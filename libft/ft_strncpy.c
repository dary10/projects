#include <string.h>

char	*ft_strncpy(char *s1, const char *s2, size_t n)
{
	size_t		c;

	c = 0;
	while (s2[c] != '\0' && c < n)
	{
		s1[c] = s2[c];
		c++;
	}
	while (c < n)
	{
		s1[c] = '\0';
		c++;
	}
	return (s1);
}
